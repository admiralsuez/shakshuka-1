export function getRandomCompletionMessage(usedMessageIds: string[]): CompletionMessage {
  const unused = COMPLETION_MESSAGES.filter(m => !usedMessageIds.includes(m.msgId));
  
  // If all messages used, reset and start over
  if (unused.length === 0) {
    const randomMsg = COMPLETION_MESSAGES[Math.floor(Math.random() * COMPLETION_MESSAGES.length)];
    return randomMsg;
  }
  
  // Return random unused message
  return unused[Math.floor(Math.random() * unused.length)];
}